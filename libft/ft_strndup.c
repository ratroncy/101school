#include "libft.h"

char	*ft_strndup(const char *s, unsigned int n)
{
	char			*dup;

	if (!(dup = (char*)malloc(sizeof(char) * (n + 1))))
		return (NULL);
	ft_strncpy(dup, s, n);
	return (dup);
}
