#include "libft.h"

char	*ft_strrch(const char *s, int c)
{
	int	a;

	a = 0;
	while (s[a])
		a++;
	while (a-- != 0)
	{
		if (s[a] == c)
			return ((char*)&s[a]);
	}
	return (0);
}
