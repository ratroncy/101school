#include "libft.h"

size_t		ft_ignoreblank(const char *s)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] != 32 && s[i] != '\n' && s[i] != '\t' && s[i] != ',')
			j++;
		i++;
	}
	return (j);
}
