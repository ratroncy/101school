#include "ft_header.h"

void	ft_strclr(char *s)
{
	int		i;
	int		len;

	if (!s)
		return ;
	i = 0;
	len = ft_strlen(s);
	while (i < len)
	{
		s[i] = '\0';
		i++;
	}
}
