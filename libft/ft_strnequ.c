#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	while (*s1 == *s2 && (*s1 != '\0' || *s2 != '\0') && n--)
	{
		s1++;
		s2++;
	}
	if (*s1 != *s2)
	{
		return (0);
	}
	else
		return (1);
}
